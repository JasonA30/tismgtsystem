    private void jButton2_removecourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2_removecourseActionPerformed
        
        // we deleting a course, grade must also be deleted that was affected
       
       /*
       
       ALTER TABLE score 
        add CONSTRAINT fk_score_course 
        FOREIGN KEY (`course_id`) 
        REFERENCES course (Id) 
        on DELETE CASCADE 
       
       
       
       
       */
        
        
        
        
        if(!jTextField1_course_id_manage.getText().equals(""))
        {
        
        
        int id  = Integer.valueOf(jTextField1_course_id_manage.getText());
        
         c.insertUpdateDeleteStudent('d', id, null, null);
         manageCourseForm.jTable1_course.setModel(new DefaultTableModel(null, new Object[]{"Id","Course","Hours" }));
            c.fillCourseJtable(manageCourseForm.jTable1_course);
        
            jTextField1_course_id_manage.setText("");
            jTextField1_coursecode_manage.setText("");
            jSpinner1_hours_manage.setValue(4);
            
        }
        
    }//GEN-LAST:event_jButton2_removecourseActionPerformed

