    private void jTable1_courseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1_courseMouseClicked
        
        int index = jTable1_course.getSelectedRow();
        jTextField1_course_id_manage.setText(jTable1_course.getValueAt(index, 0).toString());
        jTextField1_coursecode_manage.setText(jTable1_course.getValueAt(index, 1).toString());
        jSpinner1_hours_manage.setValue(Integer.valueOf(jTable1_course.getValueAt(index, 2).toString()));
        
        
    }//GEN-LAST:event_jTable1_courseMouseClicked

    private void jButton1_editcourseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_editcourseActionPerformed
          
            int id  = Integer.valueOf(jTextField1_course_id_manage.getText());
            String label = jTextField1_coursecode_manage.getText();
            int hours = Integer.valueOf(jSpinner1_hours_manage.getValue().toString());
            c.insertUpdateDeleteStudent('u', id, label, hours);
        
        
        
            manageCourseForm.jTable1_course.setModel(new DefaultTableModel(null, new Object[]{"Id","Course","Hours" }));
            c.fillCourseJtable(manageCourseForm.jTable1_course);
            //JOptionPane.showMessageDialog(null, "Course Edited");
       
    }//GEN-LAST:event_jButton1_editcourseActionPerformed

